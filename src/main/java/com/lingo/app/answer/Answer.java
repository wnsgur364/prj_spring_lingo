package com.lingo.app.answer;

public class Answer {

	private String seq;
	private Integer delNy;
	private Integer answerNy;
	private String answer;
	private String quiz_seq;
	
//	----
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public Integer getDelNy() {
		return delNy;
	}
	public void setDelNy(Integer delNy) {
		this.delNy = delNy;
	}
	public Integer getAnswerNy() {
		return answerNy;
	}
	public void setAnswerNy(Integer answerNy) {
		this.answerNy = answerNy;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	public String getQuiz_seq() {
		return quiz_seq;
	}
	public void setQuiz_seq(String quiz_seq) {
		this.quiz_seq = quiz_seq;
	}
	
}
